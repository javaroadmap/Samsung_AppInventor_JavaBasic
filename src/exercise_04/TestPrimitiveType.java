package exercise_04;

public class TestPrimitiveType {

	public static void main(String[] args){
		// boolean type
		boolean flag = true;
		System.out.println( "value of flag = " + flag );
		
		// char type
		char ch = 'A';
		char ch2 = '\uAC00';
		System.out.println( "value of flag = " + ch );
		System.out.println( "value of flag = " + ch2 );
		
		// int type
		int index = 3;
		System.out.println( "value of index = " + index );
		int i = 30;
		long l = 30L;
		System.out.println( "value of i is : " + i );
		System.out.println( "value of l is : " + l );
		
		// double type
		double d = 3.4;
		System.out.println( "value of d = " + d );
		float f = 3.14f;
		System.out.println( "value of f is : " + f );
		
	}
}
